export const portalsMat: number[][] = []
for (let i = 0; i < portalsTile.length; i += 30) {
    portalsMat.push(portalsTile.slice(i, i + 30 + 1))
}


export class Portal {
    position: { x: number, y: number }
    width: number
    height: number
    title: string
    static width = 96
    static height = 96

    constructor(position: { x: number, y: number }, title: string) {
        this.position = position
        this.title = title
        this.width = 96
        this.height = 96
    }

    draw() {
        c!.fillStyle = 'red'
        c?.fillRect(this.position.x, this.position.y, this.width, this.height)
    }
}

export const portals: Portal[] = []
portalsMat.forEach((row, i) => {
    row.forEach((tile, j) => {
        console.log(j, 'hello');
        if (206 > tile && tile > 200) {
            portals.push(new Portal({
                // x: i * -Portal.width - 100,
                // y: j * -Portal.height - 520,
                x: (j) * Portal.width - 100,
                y: i * Portal.height - 520
            },
                titleHelper(tile),
            ))
        }
    })
})

function titleHelper(tileNum: number): string {
    switch (tileNum) {
        case 205:
            return 'react'
        case 204:
            return 'vue'
        case 203:
            return 'svelte'
        case 202:
            return 'solid'
        case 201:
            return 'angular'
    }
}   